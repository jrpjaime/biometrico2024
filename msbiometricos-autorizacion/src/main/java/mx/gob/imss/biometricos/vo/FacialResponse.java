package mx.gob.imss.biometricos.vo;

public class FacialResponse {
	
	Integer codigo;
	String similitud1;
	String similitud2;
	
	
	public Integer getCodigo() {
		return codigo;
	}
	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}
	public String getSimilitud1() {
		return similitud1;
	}
	public void setSimilitud1(String similitud1) {
		this.similitud1 = similitud1;
	}
	public String getSimilitud2() {
		return similitud2;
	}
	public void setSimilitud2(String similitud2) {
		this.similitud2 = similitud2;
	}
	
 

}
